describe('#putCard(...)', function () {
  let sock = null

  before(function () {
    sock = setup(request, [reply])
  })

  after(function () {
    teardown(sock)
  })

  it('should execute put-card', function (done) {
    const expected = {
      deviceId: 405419896,
      stored: true
    }

    uhppoted.putCard(ctx, 405419896, 123456789, '2021-01-01', '2025-12-31', { 1: true, 2: false, 3: 29, 4: true })
      .then(response => {
        expect(response).to.deep.equal(expected)
        done()
      })
      .catch(err => done(err))
  })
})
